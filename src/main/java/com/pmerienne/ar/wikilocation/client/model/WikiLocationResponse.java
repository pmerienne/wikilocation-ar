package com.pmerienne.ar.wikilocation.client.model;

import java.util.List;

public class WikiLocationResponse {

	private List<Article> articles;

	public WikiLocationResponse() {
		super();
	}

	public WikiLocationResponse(List<Article> articles) {
		super();
		this.articles = articles;
	}

	public List<Article> getArticles() {
		return articles;
	}

	public void setArticles(List<Article> articles) {
		this.articles = articles;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((articles == null) ? 0 : articles.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		WikiLocationResponse other = (WikiLocationResponse) obj;
		if (articles == null) {
			if (other.articles != null)
				return false;
		} else if (!articles.equals(other.articles))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "Articles [articles=" + articles + "]";
	}

}
